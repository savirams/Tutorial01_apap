
public class Mahasiswa {
	int semester;
	String name;
	public Mahasiswa(int semester, String name) {
		this.semester = semester;
		this.name = name;
	}
	public int getSemester() {
		return semester;
	}
	public void setSemester(int semester) {
		this.semester = semester;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
